package com.skyfin.bean;

public class Message {
	private int messId;
	private String messUserId;
	private String messCommNum;
	private String messContent;
	public int getMessId() {
		return messId;
	}
	public void setMessId(int messId) {
		this.messId = messId;
	}
	public String getMessUserId() {
		return messUserId;
	}
	public void setMessUserId(String messUserId) {
		this.messUserId = messUserId;
	}
	public String getMessCommNum() {
		return messCommNum;
	}
	public void setMessCommNum(String messCommNum) {
		this.messCommNum = messCommNum;
	}
	public String getMessContent() {
		return messContent;
	}
	public void setMessContent(String messContent) {
		this.messContent = messContent;
	}
	
	

}
